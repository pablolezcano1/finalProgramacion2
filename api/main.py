@app.get("/users/{user_id}/tasks", response_model=List[schemas.Task])
def read_user_tasks(user_id: int, db: Session = Depends(get_db)):
    tasks = db.query(models.Task).filter(models.Task.user_id == user_id, models.Task.status != "completed").all()
    return tasks


@app.post("/users/{user_id}/tasks", response_model=schemas.Task)
def assign_task_to_user(user_id: int, task: schemas.TaskCreate, db: Session = Depends(get_db)):
    db_user = db.query(models.User).filter(models.User.id == user_id).first()
    if db_user is None:
        raise HTTPException(status_code=404, detail="Usuario no encontrado")
    db_task = models.Task(**task.dict(), user_id=user_id)
    db.add(db_task)
    db.commit()
    db.refresh(db_task)
    return db_task